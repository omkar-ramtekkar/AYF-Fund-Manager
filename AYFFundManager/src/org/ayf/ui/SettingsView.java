    String showDirChoosener(final boolean databaseFile)
    {
        JFileChooser fileChooser = new JFileChooser();
        
        if(databaseFile)
        {
            fileChooser.setCurrentDirectory(new File(PreferenceManager.getDatabaseDir()));
        }
        else
        {
            fileChooser.setCurrentDirectory(new File(PreferenceManager.getImageDir()));
        }
        
        fileChooser.setFileSelectionMode(databaseFile ? JFileChooser.FILES_ONLY : JFileChooser.DIRECTORIES_ONLY);

        fileChooser.setMultiSelectionEnabled(false);

        fileChooser.setFileFilter(new FileFilter()
        {
            @Override
            public boolean accept(File f) 
            {
                if(databaseFile)
                {
                    return (f.isDirectory() || f.getName().endsWith(".mdb") || f.getName().endsWith(".accdb"));
                }
                else
                {
                    return f.isDirectory();
                }
            }

            @Override
            public String getDescription() {
                if(databaseFile)
                {
                    return "*.mdb and *.accdb";
                }
                else
                {
                    return "Image Directories";
                }
            }
        });

        int userOption = fileChooser.showOpenDialog(this);

        if(userOption == JFileChooser.APPROVE_OPTION)
        {
            File file = fileChooser.getSelectedFile();
                      
            return file.getAbsolutePath();
        }
        
        return null;
    }

