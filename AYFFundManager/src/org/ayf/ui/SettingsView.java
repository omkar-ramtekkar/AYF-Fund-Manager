    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed

        boolean isValid = true;
        File databasefile = new File(this.databaseDirTxt.getText());
        if(databasefile.isDirectory() || !databasefile.exists())
        {
            isValid = false;
            Toast.showToast(this.databaseDirTxt, "Database file not present at provided path", false);
        }
        
        String imagePath = this.imageDirTxt.getText();
        if(!new File(imagePath).exists())
        {
            isValid = false;
            Toast.showToast(this.imageDirTxt, "Directory does not exist at provided path", false);
        }
        
        
        if(isValid)
        {
            String oldDatabaseDir = PreferenceManager.getDatabaseDir();
            String newDatabaseDir = this.databaseDirTxt.getText().trim();
            
            PreferenceManager.setDatabaseDir(newDatabaseDir);
            PreferenceManager.setImageDir(this.imageDirTxt.getText().trim());
            PreferenceManager.setNextRegID(this.nextRegIDTxt.getText().trim());
            PreferenceManager.setNextDonationID(this.nextDonationIDTxt.getText().trim());

            Point centerPoint = new Point((int)(Toolkit.getDefaultToolkit().getScreenSize().getWidth() / 2.0) , (int)(Toolkit.getDefaultToolkit().getScreenSize().getHeight()/ 2.0));

            Toast.showToast("Settings saved successfully.", centerPoint, true);

            setVisible(false);
            
            if(!oldDatabaseDir.equals(newDatabaseDir))
            {
                ApplicationManager.getSharedManager().databasePathDidChange(oldDatabaseDir, newDatabaseDir);
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

